static inline u32 rtl8224_mask(u8 msb, u8 lsb)
{
	int l = msb - lsb + 1;
	return (BIT(l) - 1) << lsb;
}

static inline int rtl8224_poll_wait(struct phy_device *phydev, int page,
			     u32 reg, u32 data, u32 mask)
{
	u32 val;
	return phy_read_mmd_poll_timeout(phydev, page, reg, val,
					 (val & mask) == data, 1000,
					 1000000000, false);
}

static inline int rtl8224_poll_wait_ne(struct phy_device *phydev, int page,
				u32 reg, u32 data, u32 mask)
{
	u32 val;
	return phy_read_mmd_poll_timeout(phydev, page, reg, val,
					 (val & mask) != data, 1000,
					 1000000000, false);
}

/**
 * rtl8224_sds_read allows to read a SerDes register of the chip behind phydev.
 */
int rtl8224_sds_read(struct phy_device *phydev, int page, u32 reg)
{
	int ret;
	u32 data;
	u32 cmd = RTL8224_SDS_CMD_READ | (reg << 7) | (page << 1);

	ret = phy_write_mmd(phydev, MDIO_MMD_VEND1,
			    RTL8224_REG_SDS_CMD, cmd);
	if (ret < 0)
		return ret;

	data = phy_read_mmd(phydev, MDIO_MMD_VEND1,
			    RTL8224_REG_SDS_RD_DATA);
	if (data < 0)
		return data;

	ret = rtl8224_poll_wait(phydev, MDIO_MMD_VEND1,
				RTL8224_REG_SDS_CMD, 0, BIT(15));
	if (ret < 0)
		return ret;

	return data;
}

int rtl8224_sds_write(struct phy_device *phydev, int page, int reg,
		      u32 data)
{
	int ret;
	u32 cmd = RTL8224_SDS_CMD_WRITE | (reg << 7) | (page << 1);

	ret = phy_write_mmd(phydev, MDIO_MMD_VEND1,
			    RTL8224_REG_SDS_WR_DATA, data);
	if (ret < 0)
		return ret;

	ret = phy_write_mmd(phydev, MDIO_MMD_VEND1,
			    RTL8224_REG_SDS_CMD, cmd);
	if (ret < 0)
		return ret;

	return rtl8224_poll_wait(phydev, MDIO_MMD_VEND1,
				 RTL8224_REG_SDS_CMD, 0, BIT(15));
}

/** Helper to apply a patch to a data. */
#define APPLY_CHUNK(_input, _chunk, _offset, _mask) \
	((_input & ~(_mask)) | ((_chunk << _offset) & _mask))

/**
 * rtl8224_apply_chunk writes a configuration to the chip using the appropriate
 * method. A patch "chunk" can either be a PHY patch, a TOP/MMD patch or a
 * SerDes (SDS) patch. This function writes a chunk according to its type.
 */
static int rtl8224_apply_chunk(struct phy_device *phydev, struct pchunk *p)
{
	int ret = 0;
	u32 data = 0;
	u32 mask = rtl8224_mask(p->msb, p->lsb);
	int offset = PORT_OFFSET(phydev->mdio.addr);

	if ((p->pmask & (1 << offset)) == 0)
		/* Chunk is ignored since it does not apply to this port, it is not an
		error. */
		return 0;

	switch (p->op) {
	case RTL_PATCH_OP_PHY:
		if (p->msb != 15 || p->lsb != 0) {
			data = phy_read_mmd(phydev, MDIO_MMD_VEND2, p->addr);
			if (data < 0)
				return data;
		}
		data = APPLY_CHUNK(data, p->data, p->lsb, mask);
		ret = phy_write_mmd(phydev, MDIO_MMD_VEND2, p->addr, data);
		break;
	case RTL_PATCH_OP_TOP:
	case RTL_PATCH_OP_MMD:
		if (p->msb != 15 || p->lsb != 0) {
			data = phy_read_mmd(phydev, MDIO_MMD_VEND1, p->addr);
			if (data < 0)
				return data;
		}
		data = APPLY_CHUNK(data, p->data, p->lsb, mask);
		ret = phy_write_mmd(phydev, MDIO_MMD_VEND1, p->addr, data);
		break;
	case RTL_PATCH_OP_SDS:
		if (p->msb != 15 || p->lsb != 0) {
			data = rtl8224_sds_read(phydev, p->page, p->addr);
			if (data < 0)
				return data;
		}
		data = APPLY_CHUNK(data, p->data, p->lsb, mask);
		ret = rtl8224_sds_write(phydev, p->page, p->addr, data);
		break;

	case RTL_PATCH_OP_UNKNOWN:
	case RTL_PATCH_OP_PHYW:
	case RTL_PATCH_OP_ALGO:
	case RTL_PATCH_OP_DATARAM:
	default:
		/* Unsupported patch operations */
		return -EINVAL;
	}
	return ret;
}

/**
 * rtl8224_apply_list writes a single list of patches to the devices.
 *
 * @phydev: phy_device struct representing the device that will receive the
 *          patches.
 * @offset: port offset on the device.
 * @plist: list of patches to apply.
 * @sz: length of the list.
 */
static int rtl8224_apply_list(struct phy_device *phydev, struct pchunk *plist,
			      size_t sz)
{
	int ret;
	size_t count;

	count = sz / sizeof(struct pchunk);
	for (size_t i = 0; i < count; i++) {
		ret = rtl8224_apply_chunk(phydev, &plist[i]);
		if (ret < 0)
			return ret;
	}
	return 0;
}

/**
 * rtl8224_apply_patches configures the various areas of the chip with the
 * provided patch sequences.
 */
static int rtl8224_apply_patches(struct phy_device *phydev,
				 int version, struct pchunk *fwpr,
				 size_t fwpr_sz, struct pchunk *fwlm,
				 size_t fwlm_sz, struct pchunk *afe,
				 size_t afe_sz, struct pchunk *sds,
				 size_t sds_sz)
{
	int ret;

	phy_set_bits_mmd(phydev, MDIO_MMD_VEND2, 0xb820, BIT(4));
	ret = rtl8224_poll_wait(phydev, MDIO_MMD_VEND2, 0xb800,
				BIT(6), BIT(6));
	if (ret < 0)
		return ret;

	ret = rtl8224_apply_list(phydev, fwpr, fwpr_sz);
	if (ret < 0)
		return ret;

	phy_clear_bits_mmd(phydev, MDIO_MMD_VEND2, 0xb820, BIT(4));
	ret = rtl8224_poll_wait(phydev, MDIO_MMD_VEND2, 0xb800, 0,
				BIT(6));
	if (ret < 0)
		return ret;

	phy_set_bits_mmd(phydev, MDIO_MMD_VEND2, 0xa4a0, BIT(10));
	ret = rtl8224_poll_wait(phydev, MDIO_MMD_VEND2, 0xa600, 1,
				0xff);
	if (ret < 0)
		return ret;

	ret = rtl8224_apply_list(phydev, fwlm, fwlm_sz);
	if (ret < 0)
		return ret;

	ret = rtl8224_apply_list(phydev, afe, afe_sz);
	if (ret < 0)
		return ret;

	if (version == RTL8224_REV_A) {
		phy_write_mmd(phydev, MDIO_MMD_VEND2, 0xa5d0, 0);
		phy_clear_bits_mmd(phydev, MDIO_MMD_VEND2, 0xa430, 0x7);
	}

	phy_set_bits_mmd(phydev, MDIO_MMD_VEND2, 0xa47e, BIT(6) | BIT(7));

	phy_clear_bits_mmd(phydev, MDIO_MMD_VEND2, 0xa4a0, BIT(10));
	ret = rtl8224_poll_wait_ne(phydev, MDIO_MMD_VEND2, 0xa600,
				   0x1, 0xff);
	if (ret < 0)
		return ret;

	ret = rtl8224_apply_list(phydev, sds, sds_sz);
	if (ret < 0)
		return ret;

	return 0;
}

int rtl8224_patch(struct phy_device *phydev)
{
	int ret;
	int port_offset;
	struct rtl8224_shared_private *priv = phydev->shared->priv;
	struct pchunk *fwpr_patch;
	struct pchunk *fwlm_patch;
	struct pchunk *afe_patch;
	struct pchunk *sds_patch;
	size_t fwpr_sz, fwlm_sz, afe_sz, sds_sz;

	port_offset = PORT_OFFSET(phydev->mdio.addr);

	switch (priv->version) {
	case RTL8224_REV_A:
		fwpr_patch = rtl8224_a_patch_fwpr_conf;
		fwlm_patch = rtl8224_a_patch_fwlm_conf;
		afe_patch = rtl8224_a_patch_afe_conf;
		sds_patch = rtl8224_a_patch_sds_conf;
		fwpr_sz = sizeof(rtl8224_a_patch_fwpr_conf);
		fwlm_sz = sizeof(rtl8224_a_patch_fwlm_conf);
		afe_sz = sizeof(rtl8224_a_patch_afe_conf);
		sds_sz = sizeof(rtl8224_a_patch_sds_conf);
		break;
	case RTL8224_REV_B:
		fwpr_patch = rtl8224_b_patch_fwpr_conf;
		fwlm_patch = rtl8224_b_patch_fwlm_conf;
		afe_patch = rtl8224_b_patch_afe_conf;
		sds_patch = rtl8224_b_patch_sds_conf;
		fwpr_sz = sizeof(rtl8224_b_patch_fwpr_conf);
		fwlm_sz = sizeof(rtl8224_b_patch_fwlm_conf);
		afe_sz = sizeof(rtl8224_b_patch_afe_conf);
		sds_sz = sizeof(rtl8224_b_patch_sds_conf);
		break;
	default:
		fwpr_patch = rtl8224_patch_fwpr_conf;
		fwlm_patch = rtl8224_patch_fwlm_conf;
		afe_patch = rtl8224_patch_afe_conf;
		sds_patch = rtl8224_patch_sds_conf;
		fwpr_sz = sizeof(rtl8224_patch_fwpr_conf);
		fwlm_sz = sizeof(rtl8224_patch_fwlm_conf);
		afe_sz = sizeof(rtl8224_patch_afe_conf);
		sds_sz = sizeof(rtl8224_patch_sds_conf);
		break;
	}

	ret = rtl8224_apply_patches(phydev, priv->version,
				    fwpr_patch, fwpr_sz, fwlm_patch, fwlm_sz,
				    afe_patch, afe_sz, sds_patch, sds_sz);
	if (ret < 0)
		return ret;

	u32 rev = RTL8224_FW_REV_NEXT;
	if (priv->version == RTL8224_REV_A || priv->version == RTL8224_REV_B) {
		rev = RTL8224_FW_REV_AB;
	}
	rtl8224_sram_write(phydev, port_offset, 0x801e, rev);

	return 0;
}
