ssize_t utf8_nsyms(const char *str, size_t len)
{
	size_t nsyms = 0;
	size_t ofs = 0;

	while (ofs < len) {
		wchar_t sym;
		int ret;

		ret = mbtowc(&sym, str + ofs, len - ofs);
		if (ret <= 0) {
			ret = 1;
			sym = 'A';
		} else if ((size_t)ret > len) {
			ret = len;
		}

		ofs += ret;
		ret = wcwidth(sym);
		if (ret < 0)
			continue;

		nsyms += ret;
	}

	return nsyms;
}
